def make_layers():
    layers = []
    in_channels = 1
    for v in [64, "M", 128, "M", 256, 256, "M", 512, 512, "M"]:
        if v == "M":
            layers += [nn.MaxPool2d(kernel_size=2, stride=2)]
        else:
            conv2d = nn.Conv2d(in_channels, v, kernel_size=3, padding=1)
            bn = nn.BatchNorm2d(v)
            layers += [conv2d, bn, nn.ReLU(inplace=True)]
            in_channels = v
    return nn.Sequential(*layers)


class VGGish(nn.Module): 
    def __init__(self, features): 
        super(VGGish, self).__init__()
        self.features = features
        self.embeddings = nn.Sequential(
            nn.Linear(512 * 4 * 6, 4096),
            nn.ReLU(True),
            nn.Linear(4096, 4096),
            nn.ReLU(True),
            nn.Linear(4096, 128),
            nn.ReLU(True)
        )
        self.fc1 = nn.Linear(128, 128)
    
    def forward(self, x):
        x = self.features(x)
        x = torch.transpose(x, 1, 3)
        x = torch.transpose(x, 1, 2)
        x = x.contiguous()
        x = x.view(x.size(0), -1)
        x = self.embeddings(x)  # N, 128
        x = self.fc1(x)

        return torch.squeeze(x)


class avolFeat(nn.Module): 
    def __init__(self): 
        super(avolFeat, self).__init__()
        self.aud_model = VGGish(make_layers())
        self.vision_model = Resnet18OTS(large_feature_map=0)
        self.eps = 1e-7
        
    def forward(self, img, aud): 
        B = aud.shape[0]
        feat_img = self.vision_model(img)  # N, 128, p_size, p_size
        feat_aud = self.aud_model(aud).view(B, 128, 1)  # N, 128,1
        
        # l-2 normalization of the features
        feat_img = feat_img / (feat_img.pow(2).sum(dim=1, keepdim=True).sqrt() + self.eps)
        feat_aud = feat_aud / (feat_aud.pow(2).sum(dim=1, keepdim=True).sqrt() + self.eps)

        return feat_img, feat_aud 
    